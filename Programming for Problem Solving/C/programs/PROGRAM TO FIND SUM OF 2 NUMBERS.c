//PROGRAM TO FIND SUM OF 2 NUMBERS

#include <stdio.h>

int main()
{
    int a,b,c;
    printf("Enter the first number: ");
    scanf("%d",&a);
    printf("\n");

    printf("Enter the second number: ");
    scanf("%d",&b);
    printf("\n");

    c=a+b;
    printf("Sum of these two numbers is: %d",c);
    printf("\n");

    return 0;
}

