// PROGRAM TO FIND AREA OF RECTANGLE
#include <stdio.h>

int main()
{
    int l,b,area;
    printf("Enter the length of rectangle in cm: ");
    scanf("%d",&l);
    printf("\n");

    printf("Enter the Breadth of rectangle in cm: ");
    scanf("%d",&b);
    printf("\n");

    area=l*b;
    printf("Area of the rectangle in cm^2 is: %d",area);
    printf("\n");

    return 0;
}



